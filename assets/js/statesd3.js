function state_chart(data) {

    const CHART_WIDTH = 1200;
    const CHART_HEIGHT = 601;

    let x = d3.scaleBand().rangeRound([0, CHART_WIDTH]).padding(0.2);
    let y = d3.scaleLinear().range([CHART_HEIGHT, 0]);

    x.domain([data].map((d) => d.Year));
    y.domain([Math.min.apply(Math, data.map(function (o) { return o.Rate; })) - .5, Math.max.apply(Math, data.map(function (o) { return o.Rate; })) + 1]);

    let chartContainer = d3
        .select('.modal')
        .select('.rate')
        .select('svg')
        .attr('width', CHART_WIDTH)
        .attr('height', CHART_HEIGHT)

    let chart = chartContainer.append('g')

    chart
        .selectAll('.bar')
        .data(data)
        .enter()
        .append('rect')
        .classed('bar', true)
        .attr('fill', '#42C7A0')
        .attr('stroke-width', 1.5)
        .attr('width', (CHART_WIDTH - 90) / 10)
        .attr('height', (data) => CHART_HEIGHT - y(data.Year))
        .attr('x', (data, i) => i * 120)
        .attr('y', (data) => ((y(data.Rate))))

    //text labels on bars
    chartContainer.append('g').selectAll("text")
        .data(data)
        .enter()
        .append("text")
        .text(function (d) { return Math.round(d.Rate * 100) / 100 })
        .attr('x', (data, i) => i * 120 + 40)
        .attr('y', (data) => y(data.Rate) + 30)
        .attr("font-family", "sans-serif")
        .attr("font-size", "15px")
        .attr("fill", "white")


    // absolute death charts
    y.domain([Math.min.apply(Math, data.map(function (o) { return o.Deaths; })) - 10, Math.max.apply(Math, data.map(function (o) { return o.Deaths; })) + 10]);

    let chartContainer1 = d3
        .select('.modal')
        .select('.abs')
        .select('svg')
        .attr('width', CHART_WIDTH)
        .attr('height', CHART_HEIGHT)

    let chart1 = chartContainer1.append('g')

    chart1
        .selectAll('.bar')
        .data(data)
        .enter()
        .append('rect')
        .classed('bar', true)
        .attr('fill', '#42C7A0')
        .attr('stroke-width', 1.5)
        .attr('width', (CHART_WIDTH - 90) / 10)
        .attr('height', (data) => CHART_HEIGHT - y(data.Year))
        .attr('x', (data, i) => i * 120)
        .attr('y', (data) => ((y(data.Deaths))) + 15)

    //text labels on bars
    chartContainer1.append('g').selectAll("text")
        .data(data)
        .enter()
        .append("text")
        .text(function (d) { return Math.round(d.Deaths * 100) / 100 })
        .attr('x', (data, i) => i * 120 + 40)
        .attr('y', (data) => y(data.Deaths) + 30)
        .attr("font-family", "sans-serif")
        .attr("font-size", "15px")
        .attr("fill", "white")
}


function handlechange() {
    d3.select(".modal").selectAll('svg').selectAll('g').remove();
    let state = document.getElementById("selectstate").value
    let modal = document.getElementById("modal")
    modal.style.display = "block";
    var span = document.getElementsByClassName("close")[0];

    span.onclick = function () {
        modal.style.display = "none";
    }

    window.onclick = function (event) {
        if (event.target == modal) {
            modal.style.display = "none";
        }
    }
    var data = state_data.filter(function (d) {
        return d.State__1 === state;
    });

    state_chart(data)
}
